"""
03. 문자열 (Strings)

이 파일에서 다루는 것:
- 문자열(string)을 만드는 방법
- 문자열을 합치고(+) 반복하고(*) 길이 재기(len)
- 문자열 메서드(method): upper, lower, strip, replace, split, join
- 인덱싱(indexing)과 슬라이싱(slicing): 글자 하나/여러 개 꺼내기
- f-문자열(f-string): 변수를 문자열에 끼워 넣기
- 이스케이프 시퀀스(escape sequence): 특수 문자 표현

문자열(string): 글자(character)들의 나열. 항상 따옴표로 감쌉니다.
"""

# ============================================================
# 1. 문자열 만들기 (Creating Strings)
# ============================================================

# 작은따옴표(single quote)와 큰따옴표(double quote) 둘 다 가능합니다.
single = 'Hello'
double = "World"

# 여러 줄 문자열은 삼중 따옴표(triple quote).
multi = """여러 줄에 걸친
문자열을 작성할 수 있습니다."""

# ============================================================
# 2. 문자열 연산 (String Operations)
# ============================================================

# 연결 (concatenation) — 문자열을 + 로 이어붙이기
greeting = "Hello" + " " + "World"  # "Hello World"

# 반복 (repetition) — 문자열을 * 로 반복
line = "-" * 30  # "------------------------------"

# 길이 (length) — 글자 개수
print(len("Python"))  # 6

# ============================================================
# 3. 문자열 메서드 (String Methods)
# ============================================================
# 메서드(method): 어떤 값에 점(.)을 찍어 호출하는 함수.
# "hello".upper() 는 "hello"라는 문자열에게 "대문자로 바꿔줘"라고 시키는 것.

print("hello".upper())              # "HELLO"     대문자로 (uppercase)
print("HELLO".lower())              # "hello"     소문자로 (lowercase)
print("  hello  ".strip())          # "hello"     양쪽 공백 제거 (strip whitespace)
print("abc".replace("bc", "ha"))    # "aha"       치환 (replace)
print("a b c".split())              # ['a', 'b', 'c']  공백 기준 분리 (split)
print("-".join(["a", "b", "c"]))    # "a-b-c"     구분자로 결합 (join)

# ============================================================
# 4. 인덱싱과 슬라이싱 (Indexing & Slicing)
# ============================================================
# 문자열의 각 글자에는 위치 번호(인덱스, index)가 있습니다.
# **컴퓨터는 0부터 셉니다.** 첫 글자가 0번, 두 번째 글자가 1번.

text = "Python"
# 인덱스:  0  1  2  3  4  5
#         P  y  t  h  o  n
# 음수:   -6 -5 -4 -3 -2 -1   ← 뒤에서부터 -1, -2 ...

# 인덱싱 (indexing): 글자 하나 꺼내기
print(text[0])    # "P"   첫 번째 글자
print(text[-1])   # "n"   마지막 글자

# 슬라이싱 (slicing): [시작:끝] — 끝은 포함 안 함!
print(text[1:4])  # "yth"   1번부터 3번까지 (4번 미포함)
print(text[:3])   # "Pyt"   처음부터 2번까지
print(text[3:])   # "hon"   3번부터 끝까지

# ============================================================
# 5. 문자열 포매팅 (String Formatting) — f-string
# ============================================================
# f-string: 문자열 앞에 f 를 붙이고, { } 안에 변수를 넣으면 값이 들어갑니다.
# (Python 3.6+ 권장 방식)

name = "Alice"
age = 25

print(f"이름: {name}")              # "이름: Alice"
print(f"{name}은 {age}살입니다.")    # "Alice은 25살입니다."
print(f"내년 나이: {age + 1}")       # 중괄호 안에서 계산도 가능

# 소수점 자릿수 지정: :.Nf  (N자리까지)
pi = 3.14159
print(f"원주율: {pi:.2f}")           # "원주율: 3.14"

# ============================================================
# 6. 이스케이프 시퀀스 (Escape Sequences)
# ============================================================
# 백슬래시(\) + 특수 문자로 일반 글자로는 표현 못 하는 것을 나타냅니다.

print("탭:\t구분")           # \t = 탭(tab)
print("줄바꿈:\n다음 줄")    # \n = 줄바꿈(newline)
print("따옴표: \"안녕\"")    # \" = 큰따옴표 그대로

# ============================================================
# 실행 (Run Examples)
# ============================================================
if __name__ == "__main__":
    print("\n=== 이메일에서 사용자명/도메인 분리 ===")
    email = "user@example.com"
    username, domain = email.split("@")
    print(f"사용자명: {username}")  # user
    print(f"도메인: {domain}")      # example.com
